def check_python_version():
    """Check if Python version is compatible"""
    min_version = (3, 8)
    current_version = sys.version_info[:2]

    if current_version < min_version:
        print(
            f"âŒ Python {min_version[0]}.{min_version[1]}+ required. You have {current_version[0]}.{current_version[1]}")
        return False

    print(f"âœ… Python {current_version[0]}.{current_version[1]} detected")
    return True


def install_requirements():
    """Install required Python packages"""
    print("\nğŸ“¦ Installing required packages...")

    try:
        # Check if requirements.txt exists
        if not Path("requirements.txt").exists():
            print("âŒ requirements.txt not found")
            return False

        # Install packages
        result = subprocess.run([
            sys.executable, "-m", "pip", "install", "-r", "requirements.txt"
        ], capture_output=True, text=True)

        if result.returncode == 0:
            print("âœ… All packages installed successfully")
            return True
        else:
            print(f"âŒ Package installation failed: {result.stderr}")
            return False

    except Exception as e:
        print(f"âŒ Error during installation: {e}")
        return False


